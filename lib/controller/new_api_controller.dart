import 'dart:convert';

import 'package:http/http.dart' as http;
import 'package:news_api_project/model/new_category_model.dart';

//for home screen
class NewApiController {
  List<NewsModel> dataStore = [];
  Future<void> getNews() async {
    Uri url = Uri.parse(
        "https://newsapi.org/v2/top-headlines?country=us&apiKey=2c24f493e03c40e9bcafa1e9b101efa6");
    var respone = await http.get(url);
    var jsonData = jsonDecode(respone.body);
    if (jsonData["status"] == "ok") {
      jsonData["articles"].forEach((element) {
        if (element["urlToImage"] != null &&
            element["description"] != null &&
            element["author"] != null &&
            element["content"] != null) {
          NewsModel newsModel = NewsModel(
            title: element['title'],
            urlToImage: element['urlToImage'],
            description: element['description'],
            author: element['author'],
            content: element['content'],
          );
          dataStore.add(newsModel);
        }
      });
    }
  }
}

